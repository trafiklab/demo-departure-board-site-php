<h1>Trafiklab departure board demo</h1>
<p>This demo project shows you how you can build a departure board based on the Trafiklab APIs. It loads data from the
    <a href="https://www.trafiklab.se/api/resrobot-stolptidtabeller-2">ResRobot Stolptidstabeller API</a>
    using the <a href="https://github.com/trafiklab/ResRobot-php-sdk">ResRobot PHP SDK</a>, or from the
    <a href="https://www.trafiklab.se/api/sl-realtidsinformation-4">SL Realtidsinformation 4 API</a>
    using the <a href="https://github.com/trafiklab/sl-php-sdk">SL PHP SDK</a></p>.
<p>Type a station name in the sidebar to search for departures.</p>
